    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
         int selectedRowIndex = tblRestaurants.getSelectedRow();
        if(selectedRowIndex < 0){
            JOptionPane.showMessageDialog(this, "Please select a row to delete");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblRestaurants.getModel();
        CommunityAdmin selectedCommunityAdmin = (CommunityAdmin)model.getValueAt(selectedRowIndex, 0);
        // First delete the customer from employee
        
        // And then delete the userAccount
        this.system.getUserAccountDirectory().deleteUserAccount(
                this.system.getCommunityAdminDirectory().returnCommunityAdminDetails().
                        get(selectedRowIndex).returnUserAccount()
        );
        // finally delete the user from customer directory
        this.system.getCommunityAdminDirectory().deleteCommunityAdmin(selectedCommunityAdmin);
        
        JOptionPane.showMessageDialog(this, "Deleted the restaurant Successfully");

        this.populateTable();
    }//GEN-LAST:event_btnDeleteActionPerformed

