    private void populateTable(){
        System.out.println("Inside method to populate Community Admin details table");
        DefaultTableModel model = (DefaultTableModel) tblRestaurants.getModel();
        model.setRowCount(0);

        for(CommunityAdmin Cad : this.system.getCommunityAdminDirectory().returnCommunityAdminDetails()){
            System.out.println(Cad);
            Object[] row = new Object[8];
            row[0] = Cad;
            row[1] = Cad.getUserName();
            row[2] = Cad.getGender();
            row[3] = Cad.getAge();
            row[4] = Cad.getAddress();
            row[5] = Cad.getCity();
            row[6] = Cad.getCommunity();
            row[7] = Cad.getPhoneNumber();
            model.addRow(row);
        }
    }

