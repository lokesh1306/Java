    private void txtUsernameKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtUsernameKeyReleased
        // TODO add your handling code here:
        int chkpid=0;
        if(evt.getKeyChar() == java.awt.event.KeyEvent.VK_TAB || evt.getKeyChar() == java.awt.event.KeyEvent.VK_ENTER){
            for(Patients p : patD.returnPatientsDetails()){
                if(p.getUserName() == null ? txtUsername.getText() == null : p.getUserName().equals(txtUsername.getText())){
                    txtName.setText(p.getName());
                    txtAddress.setText(p.getAddress());
                    txtAge.setText(String.valueOf(p.getAge()));
                    cbGender.setSelectedItem(p.getGender());
                    CbxCommunity.setSelectedItem(p.getCommunity());
                    CbxCity.setSelectedItem(p.getCity());
                    
                    txtPhoneNumber.setText(p.getPhoneNumber());
                    chkpid++;
                }
            }
            if(chkpid==0){
                txtName.setText("");
                txtAddress.setText("");
                txtAge.setText("");
                cbGender.setSelectedIndex(0);
                CbxCommunity.setSelectedIndex(0);
                CbxCity.setSelectedIndex(0);
                txtAddress.setText("");
                txtPhoneNumber.setText("");
                JOptionPane.showMessageDialog(this,"This Id does not exist in Person Directory.Please add the person.");
            }
        }
        else{
//            JOptionPane.showMessageDialog(this,"Please enter patient id.");
        }
    }//GEN-LAST:event_txtUsernameKeyReleased

