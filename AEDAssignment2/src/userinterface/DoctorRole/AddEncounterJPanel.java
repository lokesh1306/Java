 private boolean CheckTemp(int Age, Double temp) {
        boolean res=false;
        
        if(Age>12 && (temp>100.0 || temp <95.0)) res=true;
        else if((Age<=12 && Age >3)&& (temp>99.0 || temp <95.0)) res=true;
        else if(Age<=3 && (temp>100.4 || temp <95.0)) res=true;
        return res;
    }

    private boolean CheckWt(int Age, Double wt, String Gender) {
        boolean res=false;
        if(Age<3){
            if(wt>26) res=true;
        }
        if(Age>2 && Age<13){
            if(Gender=="Female" && (wt<26 || wt>90) ) res=true;
            else if(Gender!="Female" && (wt<27 || wt>89) ) res=true;
        }
        if(Age>12 && Age<26){
            if(Gender=="Female" && (wt<90 || wt>130) ) res=true;
            else if(Gender!="Female" && (wt<90 || wt>158) ) res=true;
        }
        if(Age>25){
            if(Gender=="Female" && wt>155 ) res=true;
            else if(Gender!="Female" && wt>180 ) res=true;
        }
        return res;
    }

    private boolean CheckBp(int Age, Double Bps, Double Bpd) {
        boolean res=false;
        if(Age<=2){
            if((Bps<83 || Bps>117) && (Bpd<38 || Bpd>76)) res=true;
        }
        if(Age>=3 && Age<=8){
            if((Bps<=101 || Bps>=106) && (Bpd<=61 || Bpd>=71)) res=true;
        }
        if(Age<=9 && Age>=14){
            if((Bps<=110 || Bps>=120) && (Bpd<=72 || Bpd>=77)) res=true;
        }
        if(Age>=15 && Age<=20){
            if((Bps<115 || Bps>120) && (Bpd<78 || Bpd>80)) res=true;
        }
        if(Age>20 && Age<31){
            if((Bps<119 || Bps>122) && (Bpd<79 || Bpd>=80)) res=true;
        }
        if(Age>30 && Age<40){
            if((Bps<120 || Bps>123) && (Bpd<80 || Bpd>83)) res=true;
        }
        if(Age>=40 && Age<50){
            if((Bps<123 || Bps>126) && (Bpd<83 || Bpd>84)) res=true;
        }
        if(Age>=50 && Age<=60){
            if((Bps<128 || Bps>130) && (Bpd<84 || Bpd>85)) res=true;
        }
        if(Age>60){
            if((Bps<129 || Bps>135) && (Bpd<85 || Bpd>88)) res=true;
        }
        return res;
    }

    private boolean CheckResp(int Age, Double Resp) {
        boolean res=false;
        if(Age<2){
            if(Resp<30 || Resp>60) res=true;
        }
        if(Age>=2 && Age<4){
            if(Resp<25 || Resp>=40) res=true;
        }
        if(Age>3 && Age<=6){
            if(Resp<23 || Resp>=34) res=true;
        }
        if(Age>6 && Age<=12){
            if(Resp<19 || Resp>=30) res=true;
        }
        if(Age>12 && Age<=18){
            if(Resp<13 || Resp>=20) res=true;
        }
        if(Age>18){
            if(Resp<12 || Resp>=16) res=true;
        }
        return res;
    }
